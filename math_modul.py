import math

akar = math.sqrt(25)
pangkat = math.pow(2,5)
bulat_bawah = math.floor(1.5)
bulat_atas = math.ceil(1.5)
print()
print(f"akar 25     = {akar}")
print(f"2 pangkat 5 = {pangkat}")
print(f"pembulatan ke bawah dari 1.5 = {bulat_bawah}")
print(f"pembulatan ke atas dari 1.5 = {bulat_atas}")
print()